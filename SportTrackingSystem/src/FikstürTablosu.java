    private void FikstürTablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FikstürTablosuMouseClicked
        int rowIndex = FikstürTablosu.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)FikstürTablosu.getModel();

        textf_maçid_FT.setText(model.getValueAt(rowIndex, 0).toString());
        label_ligadı_FT.setText(model.getValueAt(rowIndex, 1).toString());
        label_evsahibi_FT.setText(model.getValueAt(rowIndex, 2).toString());
        label_deplasman_FT.setText(model.getValueAt(rowIndex, 3).toString());
        Date maçTarih;
        try {
            maçTarih = new SimpleDateFormat("yyyy-MM-dd").parse(model.getValueAt(rowIndex, 4).toString());
            tarih_FT.setDate(maçTarih);
        
        } catch (ParseException ex) {
            Logger.getLogger(FikstürTablosu.class.getName()).log(Level.SEVERE, null, ex);
        }
        sp_evsahibiskor_FT.setValue(model.getValueAt(rowIndex, 5));
        sp_deplasmanskor_FT.setValue(model.getValueAt(rowIndex, 6));
        

    }//GEN-LAST:event_FikstürTablosuMouseClicked

