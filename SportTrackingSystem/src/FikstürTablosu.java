    public boolean Kontrol(){
        //boşluk kontrolü
        if (label_ligadı_FT.getText().equals("") || (label_evsahibi_FT).getText().equals("") ||
                (label_deplasman_FT).getText().equals("") || tarih_FT.getDate() == null){
            JOptionPane.showMessageDialog(null, "boş alan bırakmayınız!");
            return false;
        }
        //spinnera girilen sayılar - olamaz
        else if ((Integer) sp_evsahibiskor_FT.getValue() < 0 || (Integer) sp_deplasmanskor_FT.getValue() < 0){
            JOptionPane.showMessageDialog(null, "skorlar sıfırdan küçük olamaz!");
            return false;
        }
        else
            return true;
    }
    
    private void buton_güncelle_FTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buton_güncelle_FTActionPerformed
        int id = Integer.valueOf(textf_maçid_FT.getText()); //textfieldden aldığımız için sonrasında inte dönüştürdük
        String ligAdı = label_ligadı_FT.getText();
        String ev_sahibi = label_evsahibi_FT.getText();
        String deplasman = label_deplasman_FT.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String maçTarih = dateFormat.format(tarih_FT.getDate());
        int ev_sahibi_skor =(Integer) sp_evsahibiskor_FT.getValue();
        int deplasman_skor =(Integer) sp_deplasmanskor_FT.getValue();
        int lig_id = 0;

      

        if (Kontrol()){
            //güncelleme yapılmasını sağlar
            fikstürt.EkleGüncelleSilTakım('g', id, ligAdı, ev_sahibi, deplasman, 
                    maçTarih, ev_sahibi_skor, deplasman_skor); //puanlar sınıfındaki ilgili metoda giderek güncelleme yapmayı sağlar.
        }
        //eski tablodan bir tane daha eklemek yerine üstüne ekler
        FikstürTablosu.setModel(new DefaultTableModel(null, new Object[] {"maç_id", "lig adı", "ev sahibi", "deplasman", "maç tarihi", "ev sahibi skoru", "deplasman skoru"}));
        fikstürt.fillFikstürTablosu(FikstürTablosu, "");
    }//GEN-LAST:event_buton_güncelle_FTActionPerformed

