    public boolean sayıDeğeriKontrol(){
        return ((Integer) sp_oynanan_PT.getValue() >= 0 && (Integer)sp_win_PT.getValue()>= 0 && (Integer)sp_drawn_PT.getValue()>= 0 && 
                (Integer)sp_lost_PT.getValue()>= 0 && (Integer) sp_atılan_PT.getValue()>= 0 && (Integer) sp_yenen_PT.getValue()>= 0);
    }
    
    private void buton_güncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buton_güncelleActionPerformed
        int id = Integer.valueOf(textf_id_PT.getText()); //textfieldden aldığımız için sonrasında inte dönüştürdük
        String takımAdı = label_takımadı_PT.getText();
        String ligAdı = label_ligadı_PT.getText();
        int oynanan =(Integer) sp_oynanan_PT.getValue(); //spinnerdeki değeri aldık
        int win = (Integer)sp_win_PT.getValue();
        int drawn =(Integer) sp_drawn_PT.getValue();
        int lost =(Integer) sp_lost_PT.getValue(); 
        int atılan =(Integer) sp_atılan_PT.getValue();
        int yenen =(Integer) sp_yenen_PT.getValue();
        int lig_id = 0;
       
        
            //averaj için
        int averaj = atılan - yenen;
        textf_averaj_PT.setText(String.valueOf(averaj)); //yazdırmak için stringe çevirdik
        
        //toplam puan için
        int toplamPuan = win * 3 + drawn;
        textf_puan_PT.setText(String.valueOf(toplamPuan));

        if (boşlukKontrol() && oyunSayısıKontrol() && sayıDeğeriKontrol()){ 
        //güncelleme yapılmasını sağlar
        Puanlar tk = new Puanlar();
        tk.EkleGüncelleSilTakım('g', id, takımAdı, ligAdı, oynanan, win, drawn, lost, 
                atılan, yenen, averaj, toplamPuan); //puanlar sınıfındaki ilgili metoda giderek güncelleme yapmayı sağlar.
        }
        else{ //birden çok hata varsa uyarıların hepsinin sırayla alınmasını istedim.
            if(!boşlukKontrol()){          
            JOptionPane.showMessageDialog(null, "takım ismi veya oyun sayısı geçerli değil!");
            }
        
            if (!oyunSayısıKontrol()){ 
            JOptionPane.showMessageDialog(null, "oynanan oyun sayısı ile diğer veriler eşleşmedi!");
            }
            if (!sayıDeğeriKontrol()){
            JOptionPane.showMessageDialog(null, "Girilen sayı değerleri sıfırdan küçük olamaz!");
            }
        }
        //eski tablodan bir tane daha eklemek yerine üstüne ekler
        PuanTablosu.setModel(new DefaultTableModel(null, new Object[] {"takım_id", "takım adı", "lig adı", "oynanan maç", "galibiyet", "beraberlik", "mağlubiyet", "atılan gol", "yenilen gol", "averaj", "toplam puan"}));
        puant.fillPuanTablosu(PuanTablosu, "");
    }//GEN-LAST:event_buton_güncelleActionPerformed

