    private void PuanTablosuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PuanTablosuMouseClicked
        int rowIndex = PuanTablosu.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)PuanTablosu.getModel();

        textf_id_PT.setText(model.getValueAt(rowIndex, 0).toString());
        label_takımadı_PT.setText(model.getValueAt(rowIndex, 1).toString());
        label_ligadı_PT.setText(model.getValueAt(rowIndex, 2).toString());
        sp_oynanan_PT.setValue(model.getValueAt(rowIndex, 3));
        sp_win_PT.setValue(model.getValueAt(rowIndex, 4));
        sp_drawn_PT.setValue(model.getValueAt(rowIndex, 5));
        sp_lost_PT.setValue(model.getValueAt(rowIndex, 6));
        sp_atılan_PT.setValue(model.getValueAt(rowIndex, 7));
        sp_yenen_PT.setValue(model.getValueAt(rowIndex, 8));
        textf_averaj_PT.setText(model.getValueAt(rowIndex, 9).toString());
        textf_puan_PT.setText(model.getValueAt(rowIndex, 10).toString());

    }//GEN-LAST:event_PuanTablosuMouseClicked

