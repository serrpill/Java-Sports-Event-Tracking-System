    public boolean boşlukKontrol(){
        return !(label_takımadı_PT).getText().equals("") && !(label_ligadı_PT).getText().equals("") && !((Integer)sp_oynanan_PT.getValue()).equals(0) && !(textf_id_PT.getText()).equals(""); 
    }   
    
    //galibiyet beraberlik ve mağlubiyet sayılarının toplamı oynanan oyun sayısını tutmuyorsa da çalışmayacak
    public boolean oyunSayısıKontrol(){
        int toplamOyun = (Integer)sp_win_PT.getValue() + (Integer)sp_drawn_PT.getValue() + (Integer)sp_lost_PT.getValue();
        int kontrol = (Integer) sp_oynanan_PT.getValue();
        return (toplamOyun == kontrol);
        
    }
    
